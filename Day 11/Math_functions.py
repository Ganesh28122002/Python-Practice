
# Find sqrt, ceil, floor, factorial, power of number
import math
y = int(input("Enter a number: "))
x = float(input("Enter a float number: "))
squareRoot = math.sqrt(y)
Ceil = math.ceil(x)
Floor = math.floor(x)
Factorial = math.factorial(y)
Power = math.pow(y,y)
print("Square root: ", squareRoot)
print("Ceil: ", Ceil)
print("Floor: ", Floor)
print("Factorial: ", Factorial)
print("Power: ", Power)
